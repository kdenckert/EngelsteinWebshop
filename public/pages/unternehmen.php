<h2>Engelstein UG (haftungsbeschränkt)</h2>
<p>
    Engelstein ist ein Zusammenschluss aus Tontechnikern, Musikern, Ingenieuren, Programmieren, Öffentlichkeitsabreitern
    und Geschäftsführern der weit über die gesetzlich beschreibbaren Grenzen einer Unternehmer-Gesellschaft hinaus
    reicht. Seit der Gründung im Februar des Jahres 2014 erweitert sich unser Kreis der Mitarbeitenden und Unterstützenden
    stetig. Die Strukturen die jeder Einzelne dabei einbringt sind über Jahre gewachsen
    und führen uns endlich zu einem einheitlichen Ziel zusammen: Wir wollen das bestehende Produktangebot um sinnvolle
    Beiträge erweitern und dabei selbst als kunstschaffende weiterhin tätig sein. Wir arbeiten hart daran, Strukturen
    aufzubauen, unter denen freies kreatives Schaffen für alle Beteiligten möglich ist.
</p>