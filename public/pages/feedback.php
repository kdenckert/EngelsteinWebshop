<h4>Anonymus:</h4>
<p>Frage: Was ist der Unertschied zwischen Grundidee und Philosophie?</p>

<h4>Engelstein</h4>
<p>
    Antwort: Nun ganz kurz erklärt: Jede Philosophie enthält eine Grundidee, aber nicht jede Grundidee ist gleich eine Philosophie. Der Begriff Philisophie wird seit Jahren als eine schmeichelnde Übertreibung in der Vermarktung genutzt um Ideen oder Grundsätze zu vermitteln. Auch wir  bedienen uns der positiven innewohnenden Bedeutung des Wortes Philosophie,  als eine Art Überbegriff. Der   Begriff Grundidee ist hier mehr als Synonym zur Prämisse, also dem übergeordneten Prinzip gemeint.
</p>

